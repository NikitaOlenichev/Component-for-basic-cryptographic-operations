const int shifts[16] = { 1, 1, 2, 2, 2, 2, 2, 2, 1, 2, 2, 2, 2, 2, 2, 1 };

// Функция перестановки битов
uint64_t permute(uint64_t input, const int* table, int n) {
    uint64_t output = 0;
    for (int i = 0; i < n; i++) {
        output <<= 1;
        output |= (input >> (64 - table[i])) & 1;
    }
    return output;
}

// Циклический сдвиг влево для 28 бит
uint32_t leftShift(uint32_t value, int shift) {
    return ((value << shift) | (value >> (28 - shift))) & 0x0FFFFFFF;
}

// Генерация раундовых ключей
vector<uint64_t> makeKeys(uint64_t key) {
    vector<uint64_t> keys;
    
    // Начальная перестановка ключа
    uint64_t permKey = permute(key, PC1, 56);
    
    // Разделяем на C и D по 28 бит
    uint32_t C = (permKey >> 28) & 0x0FFFFFFF;
    uint32_t D = permKey & 0x0FFFFFFF;
    
    // Генерируем 16 раундовых ключей
    for (int i = 0; i < 16; i++) {
        // Сдвигаем
        C = leftShift(C, shifts[i]);
        D = leftShift(D, shifts[i]);
        
        // Объединяем и применяем PC2
        uint64_t combined = ((uint64_t)C << 28) | D;
        uint64_t roundKey = permute(combined, PC2, 48);
        
        keys.push_back(roundKey);
    }
    
    return keys;
}

// Основная функция дешифрования блока
uint64_t decryptBlock(uint64_t block, uint64_t key) {
    // Генерируем ключи
    vector<uint64_t> keys = makeKeys(key);
    // Для дешифрования используем ключи в обратном порядке
    reverse(keys.begin(), keys.end());
    
    // Начальная перестановка
    uint64_t permBlock = permute(block, IP, 64);
    
    // Разделяем на левую и правую части
    uint32_t L = (permBlock >> 32) & 0xFFFFFFFF;
    uint32_t R = permBlock & 0xFFFFFFFF;
    
    // 16 раундов
    for (int round = 0; round < 16; round++) {
        uint32_t oldR = R;
        
        // Расширение R до 48 бит
        uint64_t expanded = 0;
        for (int i = 0; i < 48; i++) {
            expanded <<= 1;
            expanded |= (R >> (32 - E[i])) & 1;
        }
        
        // XOR с раундовым ключом
        expanded ^= keys[round];
        
        // S-box подстановка
        uint32_t sboxOut = 0;
        for (int i = 0; i < 8; i++) {
            // Берем 6 бит
            int sixBits = (expanded >> (42 - i * 6)) & 0x3F;
            
            // Определяем строку и столбец
            int row = ((sixBits & 0x20) >> 4) | (sixBits & 0x01);
            int col = (sixBits >> 1) & 0x0F;
            
            // Получаем значение из S-блока
            int val = S[i][row][col];
            sboxOut = (sboxOut << 4) | val;
        }
        
        // P-перестановка
        uint32_t pboxOut = 0;
        for (int i = 0; i < 32; i++) {
            pboxOut <<= 1;
            pboxOut |= (sboxOut >> (32 - P[i])) & 1;
        }
        
        // XOR с L и обмен
        R = L ^ pboxOut;
        L = oldR;
    }
    
    // Объединяем и делаем финальную перестановку
    uint64_t preOut = ((uint64_t)R << 32) | L;
    return permute(preOut, FP, 64);
}

// Преобразование строки hex в число
uint64_t hexToBlock(const string& hex) {
    uint64_t block = 0;
    for (char c : hex) {
        if (c == ' ') continue;
        block <<= 4;
        if (c >= '0' && c <= '9') block |= (c - '0');
        else if (c >= 'a' && c <= 'f') block |= (c - 'a' + 10);
        else if (c >= 'A' && c <= 'F') block |= (c - 'A' + 10);
    }
    return block;
}

// Преобразование числа в строку
string blockToStr(uint64_t block) {
    string result;
    for (int i = 7; i >= 0; i--) {
        char c = (block >> (i * 8)) & 0xFF;
        if (c != '\0') {  // не добавляем нулевые байты
            result += c;
        }
    }
    return result;
}

// Вывод блока в hex
void printHex(uint64_t block) {
    cout << hex << setw(16) << setfill('0') << block << dec;
}

